package com.example.zarkovic.testdesignpizzapro;

public class Prices {

    String s28, s32, s38;

    public Prices() {
    }

    public Prices(String s28, String s32, String s38) {
        this.s28 = s28;
        this.s32 = s32;
        this.s38 = s38;
    }

    public String getS28() {
        return s28;
    }

    public void setS28(String s28) {
        this.s28 = s28;
    }

    public String getS32() {
        return s32;
    }

    public void setS32(String s32) {
        this.s32 = s32;
    }

    public String getS38() {
        return s38;
    }

    public void setS38(String s38) {
        this.s38 = s38;
    }
}
